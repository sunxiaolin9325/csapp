#include <stdio.h>

int main(void)
{
	unsigned char uch = -1;
	signed char ch = -1;
	unsigned int uin = -1;
	signed int in = -1; 

	printf("ch = %d\n", uch);
	printf("ch = %u\n", uch);
	printf("ch = %d\n", ch);
	printf("ch = %u\n", ch);
	printf("uin = %d\n", uin);
	printf("uin = %u\n", uin);
	printf("in = %d\n", in);
	printf("in = %u\n", in);

	return 0;
}

